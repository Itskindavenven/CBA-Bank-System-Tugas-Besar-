    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        setEditDeleteBtn(true);
        
        try{
            Customer_Service cs = csControl.showCSby(findJtext.getText());
            if(cs == null){
                clearText();
                setEditDeleteBtn(false);
                JOptionPane.showConfirmDialog(null, "Data tidak ditemukan", "Konfirmasi", JOptionPane.DEFAULT_OPTION);
            }else{
                idJtext.setText(cs.getCs_id());
                namaJtext.setText(cs.getNama());
                usernameJtext.setText(cs.getUsername());
                passwordJtext.setText(cs.getPassword());
                String gaji = String.valueOf(cs.getGaji());
                gajiJtext.setText(gaji);
                
                DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                jDateChooser.setDateFormatString("yyyy-MM-dd");
                Date tanggalLahir = dateFormat.parse(cs.getTanggalLahir());
                jDateChooser.setDate(tanggalLahir);
            }
        }catch(Exception e){
            System.out.println("Error:  "+e.getMessage());
        }
    }//GEN-LAST:event_searchButtonActionPerformed

